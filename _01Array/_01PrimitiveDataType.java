package _01Array;

//Example of primitive data type stored in array



public class _01PrimitiveDataType {
	public static void main(String[] args) {
		int[] arr = new int[5];
		arr[0] = 67;
		arr[1] = 90;
		arr[2] = 70;
		arr[3] = 9;
		arr[4] = 63;
		
		for (int i = 0; i < arr.length; i++) {
			System.out.print(arr[i]+" ");
		}
		
	}
	

}
